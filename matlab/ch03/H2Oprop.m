function w = H2Oprop(dat1,dat2) 
% H2Oprop.m: properties (H,S,U,V) of H2O (water and steam) 
% Based on the revised release on the IAPWS Industrial formulation 1997 for 
% the Thermodynamic Properties of Water and Steam, August 2007 (Ref[1]). 
% To find properties of saturated H2O, use satH2Oprop.m 
% 
% Basic syntax: w = H2Oprop(dat1,dat2) 
% Inputs: 
% dat1: pressure(kPa) (range: 0 ~ 100,000 kPa) 
% dat2: temperature(deg.C) (range: 0 ~ 2000 deg.C) 
% Output: 
% w- structure of property values (H,S,U,V) of H2O 
% w.H: specific enthalpy (kJ/kg) 
% w.S: specific entropy (kJ/(kg-K)) 
% w.U: specific internal energy (kJ/kg) 
% w.V: specific volume (cm^3/g) 
% 
% Examples: 
%    x   =  H2Oprop(150,350) returns the property structure x at 150 kPa and 350 deg.C  

P = dat1/1000; % kPa -> MPa 
T = dat2 + 273.15; % deg.C -> K 
rgn = regionPT(P,T); 
switch rgn    
case 1        
w.H = H1pt(P,T); w.S = S1pt(P,T);         
w.U = U1pt(P,T); w.V = V1pt(P,T)*1e3;     
case 2        
w.H = H2pt(P,T); w.S = S2pt(P,T);         
w.U = U2pt(P,T); w.V = V2pt(P,T)*1e3;    
case 3        
w.H = H3pt(P,T);        
hs = H3pt(P,T); rhos = 1/V3ph(P,hs); w.S = S3rt(rhos,T);        
w.U = U3rt(rhos,T); w.V = V3ph(P,hs)*1e3;    
case 4        
w.H = NaN; w.S = NaN; w.U = NaN; w.V = NaN;    
case 5        
w.H = H5pt(P,T); w.S = S5pt(P,T);         
w.U = U5pt(P,T); w.V = V5pt(P,T)*1e3;    
otherwise        
w.H = NaN; w.S = NaN; w.U = NaN; w.V = NaN; 
end 
end  

% Identify regions for given P and T ------------------------------------- 
function rgn = regionPT(P,T) 
if T > 1073.15 && P < 10 && T < 2273.15 && P > 0.000611, rgn = 5; 
elseif T <= 1073.15 && T > 273.15 && P <= 100 && P > 0.000611    
if T > 623.15        
if P > rgn23(T), rgn = 3; 
if T < 647.096, val4 = rgn4T(T);                
if abs(P - val4) < 1e-5, rgn = 4; end            
end        
else, rgn = 2; end    
else        
val4 = rgn4T(T);        
if abs(P - val4) < 1e-5, rgn = 4;        
elseif P > val4, rgn = 1;        
else rgn = 2;        
end    
end 
else    
rgn = 0; % Not in valid region 
end 
end  

